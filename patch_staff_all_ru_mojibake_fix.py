def score(s: str) -> int:
    return sum(s.count(m) for m in bad_markers)

def try_decode_whole(text: str) -> str:
    # Чиним типичный случай: UTF-8 текст был прочитан как Windows-1251.
    try:
        fixed = text.encode("cp1251").decode("utf-8")
        if score(fixed) < score(text):
            return fixed
    except Exception:
        pass
    return text

changed_files = 0

for path in files:
    text = path.read_text(encoding="utf-8", errors="ignore")
    before = text

    # 1. Точечные известные замены.
    for old, new in manual.items():
        text = text.replace(old, new)

    # 2. Если файл выглядит массово битым — пробуем перекодировать весь файл.
    if score(text) > 10:
        candidate = try_decode_whole(text)
        if score(candidate) < score(text):
            text = candidate

    # 3. Ещё раз точечные замены после декодирования.
    for old, new in manual.items():
        text = text.replace(old, new)

    if text != before:
        path.write_text(text, encoding="utf-8")
        changed_files += 1
        print(f"FIXED: {path} bad_score={score(text)}")

print("DONE changed_files=", changed_files)
